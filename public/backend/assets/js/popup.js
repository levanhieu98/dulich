$(function () {
    if ($('.izi1').length) {
        $(".izi1").iziModal({
            icon: "fas fa-toggle-off",
            width: "400px",
            radius: 5,
            group: 'group1',
            headerColor: "#3f4144", //ヘッダー部分の色
            overlayColor: "rgba(0, 0, 0, 0.5)", //モーダルの背景色
            transitionIn: "fadeInUp", //表示される時のアニメーション
            transitionOut: "fadeOutDown", //非表示になる時のアニメーション
            loop: true
        });
    }
})

